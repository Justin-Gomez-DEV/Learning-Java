package Codedex;

public class Rollercoaster {
    static void main(String[] args) {
        double height = 120;
        double weight = 40;

        if (height >= 120 && weight >= 40) {
            System.out.println("Congrats! You can ride!");
        } else {
            System.out.println("Sorry, You can't ride today");
        }
    }
}
